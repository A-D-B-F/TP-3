#  Ce script permet de créer, compiler et d’exécuter des projets C#. Il reçoit en 
#  paramètre le nom d’un projet ($name) ainsi qu’une commande ($command) à 
#  exécuter sur ce projet. Il y a trois commandes possibles :
#       • Create: Crée une nouvelle solution Visual Studio avec un projet C#.
#       • Build: Compile le projet C#.
#       • Run: Exécute le projet C#.